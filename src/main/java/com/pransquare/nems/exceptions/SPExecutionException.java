package com.pransquare.nems.exceptions;

public class SPExecutionException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3955378912132768868L;

	public SPExecutionException(String message, Throwable cause) {
		super(message, cause);

	}

	public SPExecutionException(String message) {
		super(message);

	}

	public SPExecutionException(Throwable cause) {
		super(cause);

	}

}
 


